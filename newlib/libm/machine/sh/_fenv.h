#ifndef __FENV_H_
#define __FENV_H_

#include <fenv.h>

#define _FPU_MASK_SHIFT 5
#define _ROUND_MASK     3

static inline fexcept_t
_sh_get_fpscr(void) {
    int fpscr;
    __asm__("sts fpscr,%0" : "=r" (fpscr));
    return fpscr;
}

static inline void
_sh_set_fpscr(int fpscr)
{
    __asm__("lds %0,fpscr" : : "r" (fpscr));
}

#endif /* __FENV_H_ */
